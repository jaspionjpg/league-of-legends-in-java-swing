package model;

public class IconeDeInvocador {
	Long id;
	String nome;
	String imagem;
	
	public Long getId() {
		return id;
	}
	
	public String getNome() {
		return nome;
	}
	
	public String getImagem() {
		return imagem;
	}
	
	public void setId(Long id) {
		this.id = id;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public void setImagem(String imagem) {
		this.imagem = imagem;
	}
}